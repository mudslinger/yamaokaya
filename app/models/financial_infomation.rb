    #会計期
    def fiscal_period
        "第#{@year - 1994}期"
    end

    def fiscal_period_variant
        "平成#{@year - 1989}年1月期"
    end

    def self.last_5
        find_all.sort do |a,b|
            b.year <=> a.year
        end.take(5)
    end

